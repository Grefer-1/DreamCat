package Model;

/**
 *
 * @author Asus
 */
public class MyTeam {
    int id;
    String name;
    String path;
    String job;
    String description;

    public MyTeam(int id, String name, String path, String job, String description) {
        this.id = id;
        this.name = name;
        this.path = path;
        this.job = job;
        this.description = description;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getJob() {
        return job;
    }

    public void setJob(String job) {
        this.job = job;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
       
}
